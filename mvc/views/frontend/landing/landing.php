    function showMessage() {
        var messageDiv = document.getElementById("message");
        messageDiv.innerHTML = "Lời nhắn của bạn đang được gửi đến chúng tôi..."; // Thay đổi văn bản
        messageDiv.style.display = "block";

        setTimeout(function() {
            messageDiv.innerHTML = "Hehe hình như lời nhắn của bạn đi theo chiều gió rồi";
            // Chờ thêm 1 giây trước khi reload lại trang
            setTimeout(function() {
                window.location.reload(); // Reload trang sau 1 giây
            }, 2000);
        }, 2000);
        return true;
    }

    function checkForm() {
        var name = document.getElementById('name').value;
        var email = document.getElementById('email').value;
        var subject = document.getElementById('subject').value;
        var message = document.getElementById('message').value;
        var sendMessageButton = document.getElementById('sendMessageButton');

        if (name !== "" && email !== "" && subject !== "" && message !== "") {
            sendMessageButton.disabled = false;
        } else {
            sendMessageButton.disabled = true;
        }
    }
</script>